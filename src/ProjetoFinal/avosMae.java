package ProjetoFinal;

public class avosMae {
	
	private String avoPai;
	private String avoMae;
	
	public avosMae(String avoPai, String avoMae) {
		this.avoPai=avoPai;
		this.avoMae=avoMae;
	}

	public String getAvoPai() {
		return avoPai;
	}

	public void setAvoPai(String avoPai) {
		this.avoPai = avoPai;
	}

	public String getAvoMae() {
		return avoMae;
	}

	public void setAvoMae(String avoMae) {
		this.avoMae = avoMae;
	}
	
}
